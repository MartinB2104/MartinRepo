package cryptoTrading;

public class Statistics {

    private double margeAverage;
    private double rOEPerc;
    private double pAndLPerc;
    private double winPerc;
    private double longPerc;

    public double getMargeAverage() {
        return margeAverage;
    }

    public void setMargeAverage(double margeAverage) {
        this.margeAverage = margeAverage;
    }

    public double getrOEPerc() {
        return rOEPerc;
    }

    public void setrOEPerc(double rOEPerc) {
        this.rOEPerc = rOEPerc;
    }

    public double getpAndLPerc() {
        return pAndLPerc;
    }

    public void setpAndLPerc(double pAndLPerc) {
        this.pAndLPerc = pAndLPerc;
    }

    public double getWinPerc() {
        return winPerc;
    }

    public void setWinPerc(double winPerc) {
        this.winPerc = winPerc;
    }

    public double getLongPerc() {
        return longPerc;
    }

    public void setLongPerc(double longPerc) {
        this.longPerc = longPerc;
    }


}


